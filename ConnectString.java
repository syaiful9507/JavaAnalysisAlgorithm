public class ConnectString {
    public static void main(String[] args) {

        //Two String can Connect become one long string 
        System.out.println("Berikut disajikan fakta tentang kehidupan" + 
        "Yang Harus kamu tau");
        System.out.println();

        // a String can contain digit number
        System.out.println("Jumlah huruf dalam alfabet Hawaiian : 12 ");

        //a value of number can connect with string
        System.out.println("Kode Telepon Antartica : " + 672+3+3);
        System.out.println("Tahun dimana Leonardo da Vinci Menciptakan " + "the parachute : " + 1515);
        System.out.println("Terdapat : " + 24 + "Jam Sehari ");
    }
}
